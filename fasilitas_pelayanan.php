8.	Ruang TU : bersih, nyaman , berkas tersusun rapi dan Pelayanan administrasi berupa pengurusan administrasi pasien, pembuatan surat keterangan sehat maupun surat keterangan sakit, pengaduan keluhan pelanggan ditangani Oleh staf tata usaha Puskesmas Tuppu.
