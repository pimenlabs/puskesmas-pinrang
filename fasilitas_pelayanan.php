10.	Ruang Kesehatan Lingkungan: bersih, berventilasi, cukup penerangan, masih bergabung dengan ruang tata usaha.
11.	Ruang Kesehatan Reproduksi: bersih, nyaman, rapi, ada alat peraga reproduksi, poster kesehatan reproduksi bergabung dengan Klinik KIA/KB.
12.	Ruang P2M (Program Pemberantasan Penyakit Menular): bersih, nyaman, rapi, poster informasi bahaya penyakit bergabung dengan ruang Laboratorium.
13.	Ruang Layanan 24 Jam (UGD) : bersih, nyaman, rapi, mudah diakses, ada SOP emergensi, ada job-aids emergency, jadwal petugas jaga, jadwal pengecekan peralatan medis, alur pelayanan kegawatdaruratan, peralatan emergensi set, Pelayanan kesehatan berupa kasus kegawatdaruratan ditangani oleh perawat dan dokter
14.	Ruang Kamar Bersalin/Nifas : bersih, nyaman, rapi, mudah diakses, penerangan yang bagus, buka 24 jam, ada jadwal jaga, ada SOP kebidanan dan BBL, ada job-aids emergensi kebidanan, ada sterilisator, informasi telepon ambulan, Lampu sorot, bidan jaga tersedia 24 jam, kipas angin,  Persalinan Normal dan rawat inap persalinan ditangani oleh bidan profesional dan visite 	oleh dokter.
15.	Ruang MTBS : bersih, nyaman, Timbangan Bayi/Balita, Alat Ukur tinggi badan bergabung dengan ruang Imunisasi.
16.	Pojok Laktasi : nyaman, bersih, tertutup bergabung dengan KIA
17.	Kamar WC/ Toilet: bersih, tersedia air yang bersih dan cukup, ada sabun.
18.	Poli umum : Pelayanan kesehatan secara umum ditangani oleh dokter yang berkompeten dan perawat
19.	Poli Gigi : Pelayanan kesehatan gigi dan mulut ditangani oleh dokter gigi dan perawat gigi
